
/**
 * @enum LOG_LEVEL
 * @description The log level enumertion to tag your log
 */
export declare enum LOG_LEVEL {
    /**
     * @description Log level: The debug is the basic level.
     */
    DEBUG = 0,
    /**
     * @description Log level: info
     */
    INFO = 1,
    /**
     * @description Log level: warn
     */
    WARN = 2,
    /**
     * @description Log level: error
     */
    ERROR = 3,
    /**
     * @description Log level: fatal
     */
    FATAL = 4,
    /**
     * @description Log level: emergency
     */
    EMERGENCY = 5
}